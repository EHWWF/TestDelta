    public void sendRoles(String projectType) {
        int isSyncing = -1;
        PreparedStatement stmt = this.getDBTransaction().createPreparedStatement("select is_syncing from program where code = ?", -1);
        ResultSet rs = null;
        try {
            stmt.setString(1, this.getCode());
            rs = stmt.executeQuery();
            if (rs.next()) {
                isSyncing = rs.getInt("is_syncing");
                
            }
        } catch (SQLException sqle) {
            if(rs != null)
                try {
                    rs.close();
                    
                } catch (SQLException e) {
                }

            if(stmt != null)
                try {
                    stmt.close();
                } catch (SQLException e) {
                }
        }
        
        if(isSyncing == 1)
            throw new JboException("Currently data element is participating in business process or in synchronization with Primavera. Save action is disabled.");
        
        
        runStatement("begin program_pkg.send_roles(?,null,?); end;", true, getId().toString(), projectType);

        refresh();
    }

    public Set<String> getUsers(String role) {
        Set<String> users = new HashSet<String>();
        RowSetIterator it = ((RowSetIterator) getUserRoleView()).getRowSet().createRowSetIterator(null);
        it.reset();

        while (it.hasNext()) {
            UserRoleViewRow row = (UserRoleViewRow) it.next();
            if (row.getRoleCode().equalsIgnoreCase(role)) {
                users.add(row.getUserId());
            }
        }

        it.closeRowSetIterator();
        return users;
    }

