    private Map<String, CostsProbabilitySpecificViewRow> defaultProbabilities = null;

    public Map<String, CostsProbabilitySpecificViewRow> getDefaultProbabilities() {
        if (defaultProbabilities == null) {
            defaultProbabilities = new HashMap<String, CostsProbabilitySpecificViewRow>();

            RowIterator it = getCostsProbabilitySpecificView();
            it.reset();

            while (it.hasNext()) {
                CostsProbabilitySpecificViewRow row = (CostsProbabilitySpecificViewRow)it.next();
                if (!defaultProbabilities.containsKey(row.getPhaseCode())) {
                    defaultProbabilities.put(row.getPhaseCode(), row);
                }
            }
            defaultProbabilities = Collections.unmodifiableMap(defaultProbabilities);
        }
        return defaultProbabilities;
    }
    
    
