    /**
     * Gets the attribute value for the calculated attribute Probability.
     * @return the Probability
     */
    public Integer getProbability() {
        RowSetIterator it = ((RowSetIterator)getCostsProbabilityView()).getRowSet().createRowSetIterator(null);
        it.reset();

        float probability = 1f;

        while (it.hasNext()) {
            CostsProbabilityViewRow prob = (CostsProbabilityViewRow)it.next();
            if(prob != null && prob.getProbability() != null )
                probability = probability * (prob.getProbability().intValue() / 100f);
        }

        it.closeRowSetIterator();

        return Math.round(probability * 100f);
    }


